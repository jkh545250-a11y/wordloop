export type BuiltinDeckId = "designer" | "developer" | "office" | "travel";

export type VocabularyWord = {
  id: string;
  word: string;
  phonetic: string;
  meaning: string;
  example: string;
  exampleTranslation: string;
  synonyms: string[];
  extraMeanings: string[];
  extraExamples: {
    sentence: string;
    translation: string;
  }[];
};

export type BuiltinDeck = {
  id: BuiltinDeckId;
  name: string;
  description: string;
  words: VocabularyWord[];
};

type WordSeed = [
  word: string,
  meaning: string,
  synonyms?: string[],
  options?: {
    phonetic?: string;
    example?: string;
    exampleTranslation?: string;
  },
];

const deckTopics: Record<BuiltinDeckId, string> = {
  designer: "design work",
  developer: "software development",
  office: "daily office work",
  travel: "travel communication",
};

const deckTopicZh: Record<BuiltinDeckId, string> = {
  designer: "设计工作",
  developer: "软件开发",
  office: "日常办公",
  travel: "旅行沟通",
};

function makePhonetic(word: string) {
  return `/${word.toLowerCase().replace(/[^a-z]+/g, " ").trim().replace(/\s+/g, " ")}/`;
}

function sentenceCase(word: string) {
  return word.charAt(0).toUpperCase() + word.slice(1);
}

function createWords(deckId: BuiltinDeckId, seeds: WordSeed[]): VocabularyWord[] {
  return seeds.map(([word, meaning, synonyms = [], options = {}], index) => ({
    id: `${deckId}-${String(index + 1).padStart(3, "0")}`,
    word,
    phonetic: options.phonetic ?? makePhonetic(word),
    meaning,
    example: options.example ?? `${sentenceCase(word)} is useful in ${deckTopics[deckId]}.`,
    exampleTranslation:
      options.exampleTranslation ?? `${word} 常用于${deckTopicZh[deckId]}场景。`,
    synonyms,
    extraMeanings: [meaning, `${deckTopicZh[deckId]}中的相关表达`],
    extraExamples: [
      {
        sentence: `The team discussed ${word} during the session.`,
        translation: `团队在这次环节中讨论了 ${word}。`,
      },
      {
        sentence: `A clear ${word} can improve the final result.`,
        translation: `清晰的 ${word} 可以提升最终结果。`,
      },
    ],
  }));
}

function designerSeed(
  word: string,
  meaning: string,
  synonyms: string[],
  example: string,
  exampleTranslation: string,
): WordSeed {
  return [word, meaning, synonyms, { example, exampleTranslation }];
}

